print("\n=== DATASET GENERATION COMPLETE ===")
print(f"cases_raw.csv         : {len(cases_df):,} rows")
print(f"lab_results_raw.csv   : {len(lab_df):,} rows")
print(f"contacts_raw.csv      : {len(contacts_df):,} rows")
print(f"jurisdictions.csv     : {len(jurisdictions_df):,} rows")

print("\n--- Disease distribution ---")
print(cases_df['disease_name'].value_counts().to_string())

print("\n--- Missing value rates (cases) ---")
miss = cases_df.isnull().mean().mul(100).round(1)
print(miss[miss > 0].to_string())

print("\nAll raw files saved to data/raw/")
